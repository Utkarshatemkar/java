/*
 * Java Identifiers
 * An identifier in Java is the name given to Variables,Classes, Methods,
 * Packages, Interfaces, etc.
 * These are the unique names used to identify programming elements.
 * Every Java Variable must be identified with a unique name.
 * In the following Java code, we have 5 identifiers as follows: 
 Identifier: Class Name
main: Method Name
String: Predefined Class Name
args: Variable Name
a: Variable Name
int :data type
 */
public class Identifier {
    public static void main(String[] args) {
        // declaring two int variables
        int a = 10;
        int b = 20;

        System.out.println(a + b);
    }
}
